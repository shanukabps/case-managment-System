    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        PreparedStatement ps = null;
        String delt = caseid.getText();
        if (delt.equals("")) {
            JOptionPane.showMessageDialog(this, "Select any client before deletion!");

        } else {
            int p = JOptionPane.showConfirmDialog(null, "Do you really want to delete", "Delete", JOptionPane.YES_NO_OPTION);
            if (p == 0) {
                String query = "DELETE FROM AddCase WHERE CaseID=?";

                clientid.setText("");
                caseid.setText("");
                caseno.setText("");
                courtno.setText("");
                casedes.setText("");
                date.setText("");
                try {
                    ps = db.con().prepareStatement(query);
                    ps.setString(1, delt);

                    ps.execute();
                } catch (SQLException ex) {
                    Logger.getLogger(Delete.class.getName()).log(Level.SEVERE, null, ex);
                } finally {
                    try {
                        ps.close();

                    } catch (SQLException ex) {
                        Logger.getLogger(AddCase.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }
                JOptionPane.showMessageDialog(this, "Deleted");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

